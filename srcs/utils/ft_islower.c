#include "utils.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is an lower-case alphabet.                 */
/* ************************************************************************** */
int	ft_islower(int c)
{
	if ('a' <= c && c <= 'z')
		return (1);
	return (0);
}
