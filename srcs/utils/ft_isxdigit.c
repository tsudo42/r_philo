#include "utils.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is a hexadecimal-digit character.          */
/* ************************************************************************** */
int	ft_isxdigit(int c)
{
	if (('0' <= c && c <= '9') || \
		('A' <= c && c <= 'F') || \
		('a' <= c && c <= 'f'))
		return (1);
	return (0);
}
