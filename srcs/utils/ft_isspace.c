#include "utils.h"

/* ************************************************************************** */
/*  This function returns if #arg1 is one of the white-space characters.      */
/*  The white-space characters means '\t', '\n', '\v', '\f', '\r', and ' '.   */
/* ************************************************************************** */
int	ft_isspace(int c)
{
	if (('\t' <= c && c <= '\r') || c == ' ')
		return (1);
	return (0);
}
